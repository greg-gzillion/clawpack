#!/usr/bin/env python3
"""
Claw-Coder Smart - Generates Code Without Duplicates
"""

import os
import json
from pathlib import Path
from claw_scanner import ProjectScanner

class SmartClaw:
    def __init__(self):
        self.scanner = ProjectScanner()
        self.scanner._load_previous_scan()
        
        # If no scan exists, run it
        if not self.scanner.scan_data['files']:
            print("📡 First-time setup: Scanning your project...")
            self.scanner.scan_project()
    
    def check_exists(self, filename, content_type):
        """Check if file or functionality already exists"""
        existing_files = [f['path'] for f in self.scanner.scan_data['files']]
        
        # Check for exact filename match
        if filename in existing_files:
            return True, f"File '{filename}' already exists"
        
        # Check for similar contract names
        for contract in self.scanner.scan_data['contracts']:
            if contract.get('contract_name', '').lower() in filename.lower():
                return True, f"Contract similar to '{contract.get('contract_name')}' already exists at {contract['path']}"
        
        return False, None
    
    def suggest_name(self, desired_name, file_type):
        """Suggest an unused filename"""
        existing_files = [f['path'] for f in self.scanner.scan_data['files']]
        
        base_name = desired_name
        counter = 1
        while f"{base_name}.{file_type}" in existing_files:
            base_name = f"{desired_name}_{counter}"
            counter += 1
        
        return f"{base_name}.{file_type}"
    
    def generate_new_code(self, request):
        """Generate code only if it doesn't already exist"""
        
        # Determine what to generate
        if 'token' in request.lower():
            suggested_file = self.suggest_name("phoenix_token", "rs")
            exists, msg = self.check_exists(suggested_file, "contract")
            
            if exists:
                return {
                    'can_generate': False,
                    'message': f"⚠️ {msg}\n\nUse a different name or modify existing contract.",
                    'suggested_name': suggested_file
                }
            
            return {
                'can_generate': True,
                'filename': suggested_file,
                'code': self._generate_token_contract(),
                'message': f"✅ Ready to generate new token contract as '{suggested_file}'"
            }
        
        elif 'validator' in request.lower():
            suggested_file = self.suggest_name("setup_validator", "sh")
            exists, msg = self.check_exists(suggested_file, "script")
            
            if exists:
                return {
                    'can_generate': False,
                    'message': f"⚠️ {msg}"
                }
            
            return {
                'can_generate': True,
                'filename': suggested_file,
                'code': self._generate_validator_script(),
                'message': f"✅ Ready to generate validator script as '{suggested_file}'"
            }
        
        elif 'dex' in request.lower():
            suggested_file = self.suggest_name("dex_order", "js")
            exists, msg = self.check_exists(suggested_file, "script")
            
            if exists:
                return {
                    'can_generate': False,
                    'message': f"⚠️ {msg}"
                }
            
            return {
                'can_generate': True,
                'filename': suggested_file,
                'code': self._generate_dex_code(),
                'message': f"✅ Ready to generate DEX code as '{suggested_file}'"
            }
        
        else:
            return {
                'can_generate': False,
                'message': "I need a specific request. Try: 'token', 'validator', or 'dex'"
            }
    
    def _generate_token_contract(self):
        """Generate new token contract"""
        return '''// New PhoenixPME Token Contract
// Generated by Claw-Coder - No conflicts with existing code

use cosmwasm_std::{
    entry_point, to_binary, Binary, Deps, DepsMut, Env,
    MessageInfo, Response, StdResult, Addr, Uint128
};

#[entry_point]
pub fn instantiate(
    deps: DepsMut,
    _env: Env,
    _info: MessageInfo,
    msg: InstantiateMsg,
) -> StdResult<Response> {
    // Your new token implementation here
    Ok(Response::new()
        .add_attribute("action", "instantiate")
        .add_attribute("name", msg.name))
}'''
    
    def _generate_validator_script(self):
        """Generate new validator script"""
        return '''#!/bin/bash
# New Validator Setup Script
# Generated by Claw-Coder

echo "Setting up validator..."

# Add your validator setup commands here
'''
    
    def _generate_dex_code(self):
        """Generate new DEX code"""
        return '''// New DEX Integration
// Generated by Claw-Coder

async function placeOrder() {
    // Your DEX order logic here
    console.log("Order placed");
}
'''
    
    def get_existing_contracts(self):
        """List existing contracts to avoid duplication"""
        contracts = []
        for contract in self.scanner.scan_data['contracts']:
            contracts.append({
                'name': contract.get('contract_name', contract['path']),
                'path': contract['path'],
                'messages': contract.get('execute_messages', [])
            })
        return contracts

def main():
    agent = SmartClaw()
    
    print("=" * 70)
    print("🦞 Claw-Coder Smart - No Duplicate Code")
    print("=" * 70)
    
    # Show what already exists
    print("\n📁 EXISTING CONTRACTS (to avoid duplicates):")
    for contract in agent.get_existing_contracts()[:10]:
        print(f"   • {contract['name']}")
    
    print("\n" + "=" * 70)
    print("💬 Tell me what to generate (token, validator, dex)")
    print("I'll check for conflicts before generating anything new.")
    print("=" * 70)
    
    while True:
        try:
            request = input("\n🦞 Generate: ").strip()
            
            if not request:
                continue
            
            if request.lower() == 'exit':
                break
            
            if request.lower() == 'list':
                print("\n📁 Existing contracts:")
                for contract in agent.get_existing_contracts():
                    print(f"   • {contract['name']} -> {contract['path']}")
                continue
            
            result = agent.generate_new_code(request)
            
            print(f"\n{result['message']}")
            
            if result.get('can_generate'):
                print("\n" + "=" * 50)
                print(f"📄 {result['filename']}")
                print("=" * 50)
                print(result['code'][:500])
                print("...")
                print("\n💾 Save this code? (yes/no)")
                
                if input("> ").strip().lower() in ['yes', 'y']:
                    filepath = Path("/home/greg/dev/TX") / result['filename']
                    with open(filepath, 'w') as f:
                        f.write(result['code'])
                    print(f"✅ Saved to {filepath}")
            
        except KeyboardInterrupt:
            print("\n👋 Goodbye!")
            break

if __name__ == "__main__":
    main()
